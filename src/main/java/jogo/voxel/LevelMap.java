package jogo.voxel;

import java.util.HashMap;

public class LevelMap {
    private String name;
    private int[] dimensions;
    private char[][] map_layout = {
            {'#','#','#','#','#','#','#','#','#','#','#','#','#','#','#','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','.','.','#','.','.','.','.','.','.','.','.','#','.','.','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','.','.','.','.','#','#','.','.','#','#','.','.','.','.','#'},
            {'#','E','.','.','.','#','.','.','.','.','#','.','.','.','E','#'},
            {'#','.','.','.','.','#','.','.','.','.','#','.','.','.','.','#'},
            {'#','.','.','.','.','#','.','.','.','.','#','.','.','.','.','#'},
            {'#','.','.','.','.','#','#','.','.','#','#','.','.','.','.','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','E','.','#','.','.','.','.','.','.','.','.','#','.','E','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','.','.','.','.','.','.','.','.','.','.','.','.','.','.','#'},
            {'#','#','#','#','#','#','#','#','#','#','#','#','#','#','#','#'}
    };

    private HashMap<java.lang.Character, byte[]> layout_dictionary= new HashMap<>();

    public LevelMap(String name){
        this.name = name;

        this.dimensions = new int[]{16,16};

        layout_dictionary.put('E', new byte[]{2,0,0,0,2,2});
        layout_dictionary.put('.', new byte[]{2,0,0,0,2,2});
        layout_dictionary.put('#', new byte[]{2,2,2,2,2,2});
    }

    public byte[][][] getMapBlockLayout(){
        byte[][][] map_block_layout= new byte[this.dimensions[0]][this.dimensions[1]][7];
        for (int z = 0; z < this.dimensions[1]; z++) {
            for (int x = 0; x < this.dimensions[1]; x++) {
                map_block_layout[x][z] = layout_dictionary.get(map_layout[x][z]);
            }
        }
        return map_block_layout;
    }
}
